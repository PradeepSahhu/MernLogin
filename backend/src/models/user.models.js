import mongoose from "mongoose";

// write your code here
const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
      index: true, // mainly use for searching.
    },
    password: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
  },

  { timestamps: true }
);

export const User = mongoose.model("User", userSchema);
